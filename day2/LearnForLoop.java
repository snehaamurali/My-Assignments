public class LearnForLoop {

	public static void main(String[] args) {
		//print number from 1 to 5
		//starting pt =1
		//ending pt=5
		//increment by 1 
	for(int i=1;i<=5;i++)
	{
		System.out.println(i);
		//sysout(1)
		//sysout(2)
		//sysout(3)
		//sysout(4)
		//sysout(5)
	}
	for(int i=5;i>=1;i--)
	{
		System.out.println(i);
		//sysout(1)
		//sysout(2)
		//sysout(3)
		//sysout(4)
		//sysout(5)
	}

} 
}